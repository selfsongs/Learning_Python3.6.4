##          浮点数：数学中的实数，带有小数部分
##          列表类型：由0个或多个数据祖晨的有序序列
##                    用[]表示，采用逗号,分隔各元素
##                    使用保留字in判断一个元素是否在列表中
##                    如TempStr[-1] in ['C','c']判断前者是否与列表中某个元素相同
                   


#TempConvert.py
TempStr=input("请输入带有符号的温度值:")
if TempStr[-1] in ['F','f']:
    C=(eval(TempStr[0:-1])-32)/1.8
    print("转换后的温度是{:.2f}C".format(C))
elif TempStr[-1] in ['C','c']:
    F=1.8*eval(TempStr[0:-1])+32
    print("转换后的温度是{:.2f}F".format(F))
else:
    print("输入格式错误")
