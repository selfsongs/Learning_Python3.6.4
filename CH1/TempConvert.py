##6.数字类型：整数、字符串、浮点数、列表
##          字符串：由0个或多个字符组成的有序字符序列，
##                  用一对单引号或一对双引号表示，"字符串"或'字符串'
##          字符串索引：正向递增序号和反向递减序号
